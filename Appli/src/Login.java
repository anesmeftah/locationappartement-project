    private void creer(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_creer
        // TODO add your handling code here:
        String user = Username.getText();
        String pass = new String(Password.getPassword());
        
        try{
            String host = "jdbc:derby://localhost:1527/LocAPP";
            String uName = "Root";
            String uPass = "root";
            
            Connection con = DriverManager.getConnection(host, uName, uPass);
            Statement stmt = con.createStatement();

            String SQLID = "SELECT COUNT(*) FROM CLIENTS";
            ResultSet rs = stmt.executeQuery(SQLID);
            int ID;
            if (rs.next()) {
                ID = rs.getInt(1) + 1;
                System.out.println("Count of CLIENTS: " + ID);
            } else {
                ID = 1;
            }
            
            String SQL = "INSERT INTO CLIENTS(EMAIL,ID,MDP) VALUES ('" + user + "', " + ID + ", '" + pass + "')";
            int rowsAffected = stmt.executeUpdate(SQL);
            
            if(rowsAffected > 0){
                Message.setText("Vous pouvez s'authentifier");
            }
            else{
                Message.setText("Une erreur s'est produite");
            }
        }
        catch(SQLException err){
            if ("23505".equals(err.getSQLState())) { // SQLState 23505: Unique constraint violation
            Message.setText("L'email est déjà utilisé. Veuillez en choisir un autre.");
            }
            else{
                System.out.println(err.getMessage());
            }
            
        }
    }//GEN-LAST:event_creer

